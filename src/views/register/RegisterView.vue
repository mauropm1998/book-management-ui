<script setup>
import { RegistrationDto, AuthenticationApi } from '@/services';
import { ref } from 'vue';
import { useRouter } from 'vue-router';

const registerRequest = ref(new RegistrationDto(null, null, null, null));
const authService = new AuthenticationApi();
const errorMsg = ref([]);
const router = useRouter();

const register = () => {
    errorMsg.value = [];

    authService.register(registerRequest.value, (error, data, response) => {
        if (error) {
            if (response.body.errors) {
                errorMsg.value = response.body.errors;
            }
            else {
                errorMsg.value.push(response.body.error, response.body.message);
            }
        } else {
            router.push({ name: "activate-account" });
        }
    })

}
</script>